def _play_role_tag_for_product(product):
    slug = product["slug"]

    if slug in SUPPORT_ROLE_SLUGS:
        return "support"

    if slug in PRECISION_ROLE_SLUGS:
        return "sniper"

    return "assault"


def _upgrade_potential_for_product(product):
    slug = product["slug"]

    if slug in UPGRADE_POTENTIAL_BY_SLUG:
        return UPGRADE_POTENTIAL_BY_SLUG[slug]

    product_type = product["type"]
    roles = product.get("roles", [])

    if product_type == "HPA":
        return 8

    if "sniper" in roles:
        return 8

    if product_type == "GBBR":
        return 7

    if "support" in roles:
        return 6

    if product_type == "GBB":
        return 6

    if product_type == "AEG":
        return 7

    return 5


